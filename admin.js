//==================================================
// コース管理画面
// admin.js
//==================================================

//----------------------------------
// 現在編集中の料理一覧
//----------------------------------
let currentDishes = [];
// Firestore coursesコレクション
const courseRef = window.db.collection("courses");

//==================================================
// ログイン確認
//==================================================

// Firebaseのログイン状態を監視
firebase.auth().onAuthStateChanged(user => {

    // ログインしていない場合
    if (!user) {

        alert("ログインしてください");

        location.href = "login.html";

        return;
    }

    // ログイン済みならコース一覧を取得
    loadCourses();

});


//==================================================
// コース一覧を取得
//==================================================

function loadCourses(){

    courseRef
    .orderBy("order")
    .get()

    .then(snapshot=>{

        // セレクトボックス
        const select =
            document.getElementById("courseSelect");

        // 一旦空にする
        select.innerHTML = "";

        //----------------------------------
        // Firestoreの全コースを追加
        //----------------------------------

        snapshot.forEach(doc=>{

            select.innerHTML += `
                <option value="${doc.id}">
                    ${doc.id}
                </option>
            `;

        });

        //----------------------------------
        // 最初のコースを自動表示
        //----------------------------------

        if(snapshot.size > 0){

            loadCourse();

        }

    })

    .catch(error=>{

        alert(error.message);

    });

}


//==================================================
// コース変更時
//==================================================

document
.getElementById("courseSelect")
.addEventListener("change",loadCourse);


//==================================================
// コース読込
//==================================================

function loadCourse(){

    // 選択中コース
    const id =
        document.getElementById("courseSelect").value;

    if(id === "") return;

    //----------------------------------
    // Firestoreから取得
    //----------------------------------

    courseRef
    .doc(id)
    .get()

    .then(doc=>{
        if(!doc.exists){

        alert("コースがありません");

        return;

        }
        const data = doc.data();

        //----------------------------------
        // コース時間
        //----------------------------------

        document.getElementById("duration").value =
            data.duration;

        //----------------------------------
        // 料理一覧をコピー
        //----------------------------------

        currentDishes = [...(data.dishes || [])];

        //----------------------------------
        // 画面更新
        //----------------------------------

        renderDishList();

    })
        .catch(error=>{

        alert("コースの読込に失敗しました。\n" + error.message);

    });
}
//==================================================
// 保存
//==================================================

function saveCourse(){

    //----------------------------------
    // 選択中のコース名
    //----------------------------------

    const id =
        document.getElementById("courseSelect").value;

    //----------------------------------
    // コース時間
    //----------------------------------

    const duration =
        Number(document.getElementById("duration").value);

    //----------------------------------
    // Firestore更新
    //----------------------------------

    courseRef
    .doc(id)
    .update({

        duration: duration,

        dishes: currentDishes

    })

    .then(()=>{

        alert("保存しました");

    })
    .catch(error=>{

    alert("保存に失敗しました。\n" + error.message);

    });

}


//==================================================
// コース追加
//==================================================

function addCourse(){

    //----------------------------------
    // コース名入力
    //----------------------------------

    const name =
        prompt("新しいコース名");

    if(!name) return;

    //----------------------------------
    // Firestoreへ追加
    //----------------------------------

    courseRef
    .doc(name)
    .set({

        duration:90,

        dishes:[],

        order:Date.now()

    })

    .then(()=>{

        alert("追加しました");

        loadCourses();

    })

    .catch(error=>{

    alert("追加に失敗しました。\n" + error.message);

    });

}


//==================================================
// コース削除
//==================================================

function deleteCourse(){

    //----------------------------------
    // 選択中コース
    //----------------------------------

    const id =
        document.getElementById("courseSelect").value;

    //----------------------------------
    // 確認
    //----------------------------------

    if(!confirm(id + " を削除しますか？")){

        return;

    }

    //----------------------------------
    // Firestore削除
    //----------------------------------

    courseRef
    .doc(id)
    .delete()

    .then(()=>{

        alert("削除しました");

        loadCourses();

    })

    .catch(error=>{

    alert("削除に失敗しました。\n" + error.message);

    });

}


//==================================================
// コース名変更
//==================================================

function renameCourse(){

    //----------------------------------
    // 現在のコース名
    //----------------------------------

    const oldName =
        document.getElementById("courseSelect").value;

    //----------------------------------
    // 新しいコース名
    //----------------------------------

    const newName =
        prompt("新しいコース名");

    if(!newName) return;
    if(newName === oldName){

    alert("同じ名前です");

    return;

}
    //----------------------------------
    // 元データ取得
    //----------------------------------

    courseRef
    .doc(oldName)
    .get()
 
    .then(doc=>{

        //----------------------------------
        // 新しい名前で保存
        //----------------------------------

        return courseRef
                .doc(newName)
                .set(doc.data());

    })

    .then(()=>{

        //----------------------------------
        // 古いコース削除
        //----------------------------------

        return courseRef
                .doc(oldName)
                .delete();

    })

    .then(()=>{

        alert("変更しました");

        loadCourses();

    })

    .catch(error=>{

    alert("変更に失敗しました。\n" + error.message);

    });

}
//==================================================
// 料理一覧表示
//==================================================

function renderDishList(){

    //----------------------------------
    // 一覧を表示する場所
    //----------------------------------

    const list =
        document.getElementById("dishList");

    //----------------------------------
    // 一度空にする
    //----------------------------------

    list.innerHTML = "";

    //----------------------------------
    // 料理を1件ずつ表示
    //----------------------------------

    currentDishes.forEach((dish,index)=>{

        list.innerHTML += `

        <div
            draggable="true"

            ondragstart="dragStart(${index})"

            ondragover="event.preventDefault()"

            ondrop="dropDish(${index})"
        >

            <!--料理名-->

            <input
                type="text"

                value="${dish}"

                onchange="changeDish(${index},this.value)"
            >

            <!--削除ボタン-->

            <button
                type="button"

                onclick="deleteDish(${index})"
            >

                削除

            </button>

        </div>

        `;

    });

}


//==================================================
// 料理名変更
//==================================================

function changeDish(index,value){

    //----------------------------------
    // 配列を書き換える
    //----------------------------------

    currentDishes[index] = value;

}


//==================================================
// 料理追加
//==================================================

function addDish(){

    //----------------------------------
    // 最後に追加
    //----------------------------------

    currentDishes.push("新しい料理");

    //----------------------------------
    // 再表示
    //----------------------------------

    renderDishList();

}


//==================================================
// 料理削除
//==================================================

function deleteDish(index){

    //----------------------------------
    // 指定位置を削除
    //----------------------------------

    currentDishes.splice(index,1);

    //----------------------------------
    // 再表示
    //----------------------------------

    renderDishList();

}
//==================================================
// ドラッグ＆ドロップ
//==================================================

// 今ドラッグしている料理番号
let dragIndex = null;


//--------------------------------------------------
// ドラッグ開始
//--------------------------------------------------

function dragStart(index){

    // ドラッグ中の番号を保存
    dragIndex = index;

}


//--------------------------------------------------
// ドロップ
//--------------------------------------------------

function dropDish(dropIndex){

    //----------------------------------
    // 何もドラッグしていなければ終了
    //----------------------------------

    if(dragIndex === null) return;


    //----------------------------------
    // 移動する料理を取り出す
    //----------------------------------

    const item =
        currentDishes.splice(dragIndex,1)[0];


    //----------------------------------
    // ドロップ位置へ挿入
    //----------------------------------

    currentDishes.splice(dropIndex,0,item);


    //----------------------------------
    // ドラッグ状態をリセット
    //----------------------------------

    dragIndex = null;


    //----------------------------------
    // 再表示
    //----------------------------------

    renderDishList();

}


//==================================================
// HTMLから呼び出せるように公開
//==================================================

window.dragStart = dragStart;
window.dropDish = dropDish;

window.addDish = addDish;
window.deleteDish = deleteDish;
window.changeDish = changeDish;